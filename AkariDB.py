def levelmode(x, mode=0):
    if mode == 0:
        return 2700 * x ** 1.5527
    elif mode == 1:
        return 15 * x ** 2.5
    elif mode == 2:
        return 6005 * x ** 1.4


def levelget(exp, mode=0, all=None):
    x = 1
    lvlexp = levelmode(1, mode)
    while True:
        if exp > lvlexp:
            exp -= lvlexp
            x += 1
            lvlexp = levelmode(x, mode) - levelmode(x - 1, mode)
        elif all:
            return [x, int(exp), int(lvlexp)]
        else:
            return x


def uniquesum(l):
    new = []
    dinds = []
    index = -1
    for i in range(len(l)):
        if l[i] not in dinds:
            index += 1
            new.append(l[i])
            dinds.append(l[i])
            for j in range(i + 1, len(l)):
                if l[i] == l[j]:
                    new[index] += l[j]
    return new


