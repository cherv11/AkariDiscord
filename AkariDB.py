def listsplit(f, cc=10):
    r = []
    while len(f) > 0:
        if len(f) < cc:
            r.append(f)
            f = []
        else:
            r.append(f[:cc])
            f = f[cc:]
    return r


def sort(l):
    a = [v for v in l]
    for i in range(len(a) - 1):
        for j in range(len(a) - i - 1):
            if a[j] < a[j + 1]:
                a[j], a[j + 1] = a[j + 1], a[j]
    return a


